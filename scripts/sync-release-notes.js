#!/usr/bin/env node
// Parses CHANGELOG.md and writes packages/theia-extensions/src/browser/release-notes.ts.
// Run: node scripts/sync-release-notes.js
// Convention: add `> tagline text` immediately after each `## X.Y.Z — YYYY-MM-DD` heading.

const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const CHANGELOG = path.join(ROOT, "CHANGELOG.md");
const OUTPUT = path.join(
  ROOT,
  "packages/theia-extensions/src/browser/release-notes.ts",
);

const lines = fs.readFileSync(CHANGELOG, "utf8").split("\n");

const entries = [];
let current = null;

for (const line of lines) {
  const versionMatch = line.match(/^## (\d+\.\d+\.\d+) — (\d{4}-\d{2}-\d{2})/);
  if (versionMatch) {
    if (current) entries.push(current);
    current = { version: versionMatch[1], date: versionMatch[2], tagline: undefined, changes: [] };
    continue;
  }
  if (!current) continue;

  const blockquoteMatch = line.match(/^>\s+(.+)/);
  if (blockquoteMatch && current.tagline === undefined) {
    current.tagline = blockquoteMatch[1].trim();
    continue;
  }

  const bulletMatch = line.match(/^- (.+)/);
  if (bulletMatch) {
    // Strip **bold** markers; renderInline handles [text](url) links.
    const clean = bulletMatch[1].replace(/\*\*([^*]+)\*\*/g, "$1");
    current.changes.push(clean);
  }
}
if (current) entries.push(current);

// CHANGELOG is newest-first; output order is already correct.

const out = [
  `// Auto-generated by scripts/sync-release-notes.js — edit CHANGELOG.md instead.`,
  `export interface ReleaseNote {`,
  `  readonly version: string;`,
  `  readonly date: string;`,
  `  readonly tagline?: string;`,
  `  readonly changes: readonly string[];`,
  `}`,
  ``,
  `export const RELEASE_NOTES: readonly ReleaseNote[] = [`,
];

for (const entry of entries) {
  out.push(`  {`);
  out.push(`    version: ${JSON.stringify(entry.version)},`);
  out.push(`    date: ${JSON.stringify(entry.date)},`);
  if (entry.tagline !== undefined) {
    out.push(`    tagline: ${JSON.stringify(entry.tagline)},`);
  }
  out.push(`    changes: [`);
  for (const c of entry.changes) {
    out.push(`      ${JSON.stringify(c)},`);
  }
  out.push(`    ],`);
  out.push(`  },`);
}

out.push(`];`);
out.push(``);

fs.writeFileSync(OUTPUT, out.join("\n"), "utf8");
console.log(`Synced ${entries.length} entries → ${path.relative(ROOT, OUTPUT)}`);
